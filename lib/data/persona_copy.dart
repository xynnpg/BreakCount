/// Centralises all persona-dependent UI strings so the rest of the codebase
/// can stay neutral. Every key has an entry for every persona + a fallback
/// in English so a missing persona id never crashes the UI.
///
/// Usage:
///   PersonaCopy.get('hype', 'countdown_urgent', vars: {'days': '3'});
///
/// Template variables are `{name}` — not Dart string interpolation — so keys
/// can be authored in any language without compiler help.
library;

class PersonaCopy {
  /// Returns the copy for [personaId] × [key]. Interpolates `{var}` tokens
  /// from [vars]. Falls back to the 'hype' entry, then to [fallback], then to
  /// an empty string — never throws.
  static String get(
    String personaId,
    String key, {
    Map<String, Object>? vars,
    String? fallback,
  }) {
    String? template = _copy[personaId]?[key];
    template ??= _copy['hype']?[key];
    template ??= fallback ?? '';
    return _interpolate(template, vars);
  }

  static String _interpolate(String template, Map<String, Object>? vars) {
    if (vars == null || vars.isEmpty) return template;
    var out = template;
    vars.forEach((k, v) {
      out = out.replaceAll('{$k}', v.toString());
    });
    return out;
  }

